<h2>Location Service</h2>
<p>Privacy를 생각한 위치 서비스입니다.</p>
<p>AES-256 CBC IV를 사용하였습니다.</p>
<p>IV를 쓰는 이유는, 같은 값으로 암호화 할 때 마다 다른 결과값이 나오도록 하기 위해 IV를 사용하며,</p>
<p>GPS로 위치 값을 계산 할 때 마다 random한 16자리 String으로 IV와 함께 암호화에 이용되어,</p>
<p>해커 입장에서 이게 같은 값을 보내고 있는지 모르게 하여, 같은 패턴임을 모르게 합니다.</p>
<p>최고 서버 관리자는 암호화 된 값만 저장하기에, <b>여러분의 위치를 해독할 수 없습니다.</b></p>
<p>최고 서버 관리자는, 여러분의 기기와 위치 정보를 식별하기 위한 아이디, 인증키만을 기록하며, 복호화에 사용되는 기록은 수집하지 않습니다.</p>
<p>저희는 암호 해독에 필요한 키를 절대로 수집하지 않습니다.</p>
<p>여러분의 핸드폰에서 암호화 처리, 여러분의 웹 브라우저에서 복호화 처리를 합니다. <b>(*주의)공유하기 기능을 통해 들어간 URL은 링크를 알고있는 사람은 여러분과 같은 권한을 갖게 되며 링크를 알고 있는 사람은 위치를 조회할 수 있게 됩니다.</b></p>
<p>핸드폰에서는 여러분의 디바이스 식별 ID, 암호화된 위치정보, IV를 수집하고</p>
<p>서비스 유지를 위한 여러분의 접속 기록만을 수집하며,</p>
<p>악성 접속 기록은 차단될 수 있습니다.</p>
<p>위치 정보는 여러분만이 웹 브라우저를 통해서만 복호화 할 수 있도록 설계했습니다.</p>
<p>웹 서버는 Cloudflare Pages를 쓰며,</p>
<p>데이터 저장 처리(API서버)는 Cloudflare Wrangler 를 쓰며,</p>
<p>실제 데이터가 저장되는 곳은 Cloudflare D1(SQLite) 입니다. </p>
<p>iOS는 Swift를 사용하며,CryptoSwift 로 암호화 처리를 하며, 암호화 된 값을 API 서버로 보냅니다.</p>
<p>데이터 규모가 커지면, 다른 DBMS, 또는 Engine 적용될 수 있습니다.</p>
<p>위치 데이터는 최대 1일~20일동안 보관됩니다. Alpha 기간동안 SLI, SLA가 보장되지 않습니다. </p>
<p>디바이스 ID, 인증코드, 패스워드는 어플리케이션에서 확인하실 수 있습니다.</p>
